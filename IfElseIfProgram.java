package module1;

public class IfElseIfProgram {
	public static void main(String args[]) {
		int a=20;
		int b=10;
		if (a>b)
		{
			System.out.println("1");
		}
		else if(a<b)
		{
			System.out.println("2");
		}
		else if(a==b)
		{
			System.out.println("3");
		}
		else if(a!=b)
		{
			System.out.println("4");
		}
		else 
		{
			System.out.println("something wrong");
		}
	}
	

}

package module1;

public class OperatorsProgram {
	public static void main(String args[] ) {
		int a=20;
		int b=10;
		int c;
		System.out.println(c=a+b);
		System.out.println(c=a-b);
		System.out.println(c=a*b);
		System.out.println(c=a/b);
		System.out.println(c=a%b);
		add();
		sub();
		multi();
		divide();
		mod();
		
	}
		static void add() {
			int a=2;
			int b=1;
			int c;
			c=a+b;
			System.out.println(c);
		}
		static void sub() {
			int a=2;
			int b=1;
			int c;
			c=a-b;
			System.out.println(c);
		}
		
		static void multi() {
			int a=2;
			int b=1;
			int c;
			c=a*b;
			System.out.println(c);
		}
		
		static void divide() {
			int a=2;
			int b=1;
			int c;
			c=a/b;
			System.out.println(c);
		}
		
		static void mod() {
			int a=2;
			int b=1;
			int c;
			c=a%b;
			System.out.println(c);
		}
		
		
	}


